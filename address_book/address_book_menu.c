int get_option(int type, const char *msg)
{
	int option = 0;

	if (type == NONE) {
		getc(stdin);
	} else if (type == CHAR) {
		option = getc(stdin);
		if (option >= '0' && option <= '9') {
			option -= '0';
		}
	} else {
		EXIT_FAILURE;
	}
	fflush(stdin);
	return option;
}

